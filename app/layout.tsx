import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.scss";
import { cn } from "@/core/lib/utils";
import NextTopLoader from "nextjs-toploader";
import { Role } from "@/types/admin/sidebar";
import { getSession } from "@/service/server/session";
import { ReactQueryProvider, ThemeProvider } from "@/core/provider";
import { Toaster } from "@/core/ui/components";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Cybertise",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const session = await getSession();

  const colors: Record<Role, string> = {
    hacker: "#BAFF00",
    company: "#4BA2E3",
    "company staff": "#4BA2E3",
    mediator: "#845EEE",
  };

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={cn(
          inter.className,
          "bg-background-page-light dark:bg-background-page-dark",
          session && "overflow-hidden"
        )}
      >
        <NextTopLoader
          color={colors[session?.user.role as Role]}
          showSpinner={false}
        />
        <ReactQueryProvider>
          <ThemeProvider
            attribute="class"
            defaultTheme="dark"
            disableTransitionOnChange
          >
            {children}
          </ThemeProvider>
          <Toaster position="top-center" />
        </ReactQueryProvider>
      </body>
    </html>
  );
}
